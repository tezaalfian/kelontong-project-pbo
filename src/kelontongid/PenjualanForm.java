    private void btn_simpanAllActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanAllActionPerformed
        // TODO add your handling code here:
        if (tabel_trx.getRowCount() < 1){
            JOptionPane.showMessageDialog(null,"Maaf, data masih kosong!");
        } else {
            try {
                String[] key = {
                    "barang_id","qty","nominal","jenis"
                };
                ArrayList<String> value = new ArrayList<String>();
                String msg = null;
                for (int row = 0; row < tabel_trx.getRowCount(); row++){
                    value.add(tabel_trx.getValueAt(row, 0).toString());
                    value.add(tabel_trx.getValueAt(row, 2).toString());
                    value.add(tabel_trx.getValueAt(row, 4).toString());
                    value.add("JUAL");
                    msg = trxModel.simpanData(key, value);
                    barangModel.editStok("-", tabel_trx.getValueAt(row, 0).toString(), tabel_trx.getValueAt(row, 2).toString());
                    value.clear();
                }
                if(msg.contains("berhasil")){
                    reset_text();
                    tabmode.setRowCount(0);
                    txt_totaltransaksi.setText("");
                }
                JOptionPane.showMessageDialog(null, msg , "Informasi", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal tersimpan ("+e.getMessage()+")" , "Informasi", JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btn_simpanAllActionPerformed

    private void btn_ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ubahActionPerformed
        // TODO add your handling code here:
        if(index < 0){
            JOptionPane.showMessageDialog(null,"Maaf, belum ada data dipilih!");
        }else if (cb_barang.getSelectedIndex() < 1){
            JOptionPane.showMessageDialog(null,"Maaf, pilih terlebih dahulu barang nya!");
        } else if (!txt_jumlah.getText().trim().matches("[0-9]+")){
            JOptionPane.showMessageDialog(null,"Jumlah stok tidak valid!");
            txt_jumlah.setText("");
            txt_jumlah.requestFocus();
        }else {
            String [] barang = cb_barang.getSelectedItem().toString().split("\\-", -1);
            try {
                int stok = Integer.parseInt(dataBarang.getString("stok"));
                if(stok < Integer.parseInt(txt_jumlah.getText())){
                    JOptionPane.showMessageDialog(null,"Maaf, Stok tidak mencukupi!");
                    txt_jumlah.setText("");
                    txt_jumlah.requestFocus();
                }else{
                    tabel_trx.setValueAt(barang[0], index, 0);
                    tabel_trx.setValueAt(barang[1], index, 1);
                    tabel_trx.setValueAt(txt_jumlah.getText(), index, 2);
                    tabel_trx.setValueAt(txt_harga.getText(), index, 3);
                    tabel_trx.setValueAt(txt_totalHarga.getText(), index, 4);
                    setTotal();
                    reset_text();
                    JOptionPane.showMessageDialog(null,"Data berhasil diubah!");
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        }
    }//GEN-LAST:event_btn_ubahActionPerformed

