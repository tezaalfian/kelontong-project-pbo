    private boolean cekTabel(String str){
        boolean result = false;
        for (int row = 0; row < tabel_trx.getRowCount(); row++){
            if(tabel_trx.getValueAt(row, 0).toString().equals(str)){
                result = true;
            }
        }
        return result;
    }
    
    private void setTotal(){
        int total = 0;
        for (int row = 0; row < tabel_trx.getRowCount(); row++){
            total += Integer.parseInt(tabel_trx.getValueAt(row, 4).toString());
        }
        txt_totaltransaksi.setText(Integer.toString(total));
    }
    
